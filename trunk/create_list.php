function check_if_list_exists($machine_friendly_name) {
	global $database_folder_path;

	$list_file_name = $database_folder_path . "/" . $machine_friendly_name;

	return file_exists($list_file_name);
}

function create_list_file($machine_friendly_name) {
	global $database_folder_path;

	$list_file_name = $database_folder_path . "/" . $machine_friendly_name;

	$file_handle = fopen($list_file_name, 'w');
	fclose($file_handle);

	return file_exists($list_file_name);
}

function save_list_data($machine_friendly_name, $title, $description)
{
	global $database_folder_path;

	$json_string = json_encode(array("title" => $title, "description" => $description, "items" => array()));

	$list_file_name = $database_folder_path . "/" . $machine_friendly_name;

	$file_handle = fopen($list_file_name, 'w');

	fwrite($file_handle, $json_string);
	fclose($file_handle);
}

function create_list($title, $description)
{
	$machine_friendly_name = get_machine_friendly_name($title);

	$name_already_used = check_if_list_exists($machine_friendly_name);

	if($name_already_used) {
		$response_array = array("errors" => array("LIST NAME ALREADY USED"));
		$json_string = json_encode($response_array);

		return $json_string;
	}

	create_list_file($machine_friendly_name);

	save_list_data($machine_friendly_name, $title, $description);

	$response_array = array("title" => $title, "description" => $description, "machine_friendly_name" => $machine_friendly_name);
	$json_string = json_encode($response_array);

	return $json_string;
}

